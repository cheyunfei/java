package ProgramLogic;
/**（1）switch语句中表达式值必须是整型、字符型、字符串型<br>
 * （2）switch(表达式) { case 常量1：语句块；break；default：语句块；}，程序一直运行到break，default有无均可<br>
 */
public class Switch { // 创建类
	public static void main(String args[]) { // 主方法
		int week = 3; // 定义int型变量week
		switch (week) { // 指定switch语句的表达式为变量week
			case 1: // 定义case语句中的常量为1
				System.out.println("Monday"); // 输出信息
				break;
			case 2: // 定义case语句中的常量为2
				System.out.println("Tuesday");
				break;
			case 3: // 定义case语句中的常量为3
				System.out.println("Wednesday");
				break;
			default: // default语句
				System.out.println("Sorry,I don't Know");
		}
		switch(week){
		case 2:System.out.println("sdddddddddddddd");break;
		default:System.out.println("dddd");
		case 1:System.out.println("ssss");break;
		}
	}
}

